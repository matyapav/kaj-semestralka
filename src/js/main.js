//TODO udelat soubor worldBuilder nebo neco takoveho
//init world
let walls = [];
let items = [];
for(let i=0;i<level.length;i++){
    for(let j=0;j<level[i].length;j++){
        switch (level[i][j]){
            case 1:
                walls.push(new Wall(j*32, i*32, 32, 32));
                break;
            case 2:
            case 3:
            case 4:
                items.push(new Item(j*32, i*32, 32, 32, im.getItem(level[i][j])));
                break;
        }
    }
}

function update(){

    //TODO taky mozna mit nejaky collision checker
    for(let i=0; i<walls.length; i++){
        if(walls[i].checkCollisionWithPlayer(player)){
            player.dx = 0;
            player.dy = 0;
        }
    }
    for(let i=0; i<items.length; i++){
        if(items[i].checkCollisionWithPlayer(player)){
            if(player.isDoingPrimaryAction()) {
                player.dx = 0;
                player.dy = 0;
                alert("You've found "+ items[i].itemInfo.name);
                alert(items[i].itemInfo.name + ' is '+items[i].itemInfo.desc);
                player.addToScore(1);
                console.log(player.score);
                items.splice(i, 1);
            }
        }
    }
    player.update();
    draw();
}
//draw
function draw() {
    clearCanvas();
    ctx.save();
    ctx.translate(-player.posX+ canvas.width/2/2 - player.w, -player.posY + canvas.height/2/2 - player.h)

    for(let i=0; i<walls.length; i++){
        walls[i].draw(ctx);
    }
    for(let i=0; i<items.length; i++){
        items[i].draw(ctx);
    }
    player.draw(ctx);
    ctx.restore()
}

function clearCanvas() {
    ctx.globalAlpha=0.7;
    ctx.fillStyle='#000000';
    ctx.fillRect(0, 0, canvas.width, canvas.height);
    ctx.globalAlpha=1;
}

setInterval(update, 10);